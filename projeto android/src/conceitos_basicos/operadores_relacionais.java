package conceitos_basicos;
public class operadores_relacionais {
    public static void main(String[] args) {
        
        int valor_1 = 20;
        int valor_2 = 10;

        System.out.println(valor_1 == valor_2);
        System.out.println(valor_1 > valor_2);
        System.out.println(valor_1 < valor_2);
        System.out.println(valor_1 <= valor_2);
        System.out.println(valor_1 >= valor_2);
        System.out.println(valor_1 != valor_2);
    }
}
